package assisted;

public class multidimensional {

			public static void main(String args[]) {
			//multidimensional array
			int[][] arr = { {2,4},{5,9}};
			
			for(int i = 0; i<2; i++) {
				for(int j = 0; j<2; j++) {
					System.out.print(arr[i][j] + " " );
					
				}
				System.out.println();
			}
			}
		

	}

